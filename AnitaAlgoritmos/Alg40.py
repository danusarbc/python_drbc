#Leia dois numeros e imprima
# Dividendo, divisor, quociente e resto

x = int(input("Entre com o primeiro numero: "))
y = int(input("Entre com o segundo numero: "))
dividendo = x
divisor = y
quociente = x / 2
resto = x % y
print("Dividendo: ", dividendo)
print("Divisor: ", divisor)
print("Quociente: ", quociente)
print("Resto: ", resto)